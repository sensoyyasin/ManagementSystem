import React from 'react';
import { Outlet, Navigate } from 'react-router-dom';

const ProtectedRoute = () => {
    // Token ve token'ın bitiş süresini localStorage'dan al
    const token = localStorage.getItem('token');
    const tokenExpiry = localStorage.getItem('tokenExpiry');

    // Eğer token veya tokenExpiry mevcut değilse, giriş sayfasına yönlendir
    if (!token || !tokenExpiry) {
        return <Navigate to="/login" />;
    }

    // Token'ın geçerliliğini kontrol et
    const isValidToken = new Date(Number(tokenExpiry)) > new Date();

    // Eğer token geçerli ise, çocuk bileşeni render et (Outlet), aksi halde giriş sayfasına yönlendir
    return isValidToken ? <Outlet /> : <Navigate to="/login" />;
}

export default ProtectedRoute;
